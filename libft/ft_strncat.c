#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char			*u1;
	const char		*u2;

	u1 = s1;
	u2 = s2;
	while (*u1 != '\0')
		u1++;
	while (*u2 != '\0' && n > 0)
	{
		*u1 = *u2;
		u1++;
		u2++;
		n--;
	}
	*u1 = '\0';
	return (s1);
}

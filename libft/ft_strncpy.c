#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*d;

	d = dst;
	while (*src && len > 0)
	{
		*d = *src;
		d++;
		src++;
		len--;
	}
	while (len > 0)
	{
		*d = '\0';
		d++;
		len--;
	}
	return (dst);
}
